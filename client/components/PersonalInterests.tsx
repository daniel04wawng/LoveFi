export default function PersonalInterests() {
  const { userData, updateUserData } = useUser();
  const navigate = useNavigate();
  const [selectedInterests, setSelectedInterests] = useState<string[]>(
    userData.personalInterests || [],
  );

  const toggleInterest = (interestId: string) => {
    setSelectedInterests((prev) => {
      if (prev.includes(interestId)) {
        return prev.filter((id) => id !== interestId);
      } else {
        return [...prev, interestId];
      }
    });
  };

  const handleContinue = () => {
    updateUserData({ personalInterests: selectedInterests });
    navigate("/partner-preferences");
  };

  const handleBack = () => {
    navigate(-1);
  };

  return (
    <div className="w-full h-full bg-white flex flex-col relative max-w-sm mx-auto">
      {/* Back button */}
      <button
        onClick={handleBack}
        className="absolute left-5 top-5 w-13 h-13 rounded-[15px] border border-lovefi-border bg-white flex items-center justify-center z-10"
      >
        <svg
          width="24"
          height="24"
          viewBox="0 0 24 24"
          fill="none"
          xmlns="http://www.w3.org/2000/svg"
          className="text-lovefi-purple"
        >
          <path
            fillRule="evenodd"
            clipRule="evenodd"
            d="M15.2071 18.7071C14.8166 19.0976 14.1834 19.0976 13.7929 18.7071L7.79289 12.7071C7.40237 12.3166 7.40237 11.6834 7.79289 11.2929L13.7929 5.29289C14.1834 4.90237 14.8166 4.90237 15.2071 5.29289C15.5976 5.68342 15.5976 6.31658 15.2071 6.70711L9.91421 12L15.2071 17.2929C15.5976 17.6834 15.5976 18.3166 15.2071 18.7071Z"
            fill="currentColor"
          />
        </svg>
      </button>

      {/* Title */}
      <div className="pt-20 px-5 pb-6">
        <h1 className="text-lg font-normal leading-[150%] text-black font-[Alata]">
          Let's talk about you now! What are your{" "}
          <span className="text-lovefi-purple">personal interests?</span>
        </h1>
      </div>

      {/* Interests grid */}
      <div className="flex-1 px-5">
        <div className="grid grid-cols-2 gap-[15px]">
          {interests.map((interest) => {
            const isSelected = selectedInterests.includes(interest.id);
            return (
              <button
                key={interest.id}
                onClick={() => toggleInterest(interest.id)}
                className={`
                  w-full h-[45px] rounded-[15px] flex items-center px-3.5 transition-all duration-200
                  ${
                    isSelected
                      ? "bg-lovefi-purple text-white shadow-[0_15px_15px_0_rgba(233,64,87,0.20)]"
                      : "bg-white border border-lovefi-border text-black"
                  }
                `}
              >
                <div
                  className={`
                  w-[19px] h-[19px] flex-shrink-0 mr-2
                  ${isSelected ? "text-white" : "text-lovefi-purple"}
                `}
                >
                  {interest.icon}
                </div>
                <span className="text-sm font-normal leading-[150%] font-[Alata] truncate">
                  {interest.name}
                </span>
              </button>
            );
          })}
        </div>
      </div>

      {/* Continue button */}
      <div className="p-5 pt-6">
        <button
          onClick={handleContinue}
          className="w-full h-14 bg-lovefi-purple rounded-[15px] flex items-center justify-center"
        >
          <span className="text-white text-base font-normal leading-[150%] font-[Alata]">
            Continue
          </span>
        </button>
      </div>
    </div>
  );
}
